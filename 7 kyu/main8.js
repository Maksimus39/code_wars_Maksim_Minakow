// Учитывая массив чисел (в строковом формате), вы должны вернуть строку.
// Числа соответствуют буквам алфавита в обратном порядке: a=26, z=1 и т.д. Вы также должны учитывать '!', '?' И ' '
// , которые представлены '27', '28' и '29' соответственно.
//
// Все входные данные будут действительными.

function switcher(x){
    // Создаем объект для сопоставления чисел с буквами
    const alphabet = {
        '26': 'a', '25': 'b', '24': 'c', '23': 'd', '22': 'e', '21': 'f', '20': 'g',
        '19': 'h', '18': 'i', '17': 'j', '16': 'k', '15': 'l', '14': 'm', '13': 'n',
        '12': 'o', '11': 'p', '10': 'q', '9': 'r', '8': 's', '7': 't', '6': 'u',
        '5': 'v', '4': 'w', '3': 'x', '2': 'y', '1': 'z',
        '27': '!', '28': '?', '29': ' '
    };

    // Преобразуем массив чисел в строку
    return x.map(num => alphabet[num]).join('');
}

console.log(switcher(['24']));
console.log(switcher(['24', '12', '23', '22', '4', '26', '9', '8'])); // 'codewars'