// Напишите функцию, которая возвращает удвоенное входное значение, если только значение не является «хитрым double».
//
// Число считается хитрым double, если оно состоит из двух одинаковых половин без дополнительных цифр. Например,
// 44, 1212 и 7777 являются хитрыми double, потому что каждая половина числа одинакова (4 и 4, 12 и 12, 77 и 77).
//
// Если входное значение является хитрым double, верните его как есть. В противном случае верните значение, умноженное на 2.
//
// Примеры хитрых double чисел:
//
// 44 — состоит из двух 4
// 77 — состоит из двух 7
// 3333 — состоит из двух 33
// 8787 — состоит из двух 87
// 100100 — состоит из двух 100
// В примере 8787 число 87 удваивается (появляется дважды).

function trickyDoubles(n){
    //insert your code here
    // Преобразуем число в строку
    const str = n.toString();

    // Найдём середину строки
    const mid = Math.floor(str.length / 2);

    // Разделим строку на две части
    const firstHalf = str.slice(0, mid);
    const secondHalf = str.slice(mid);

    // Сравним две части
    if (firstHalf === secondHalf) {
        // Если части одинаковые, это хитрый double
        return n;
    } else {
        // В противном случае умножаем число на 2
        return n * 2;
    }
}

console.log(trickyDoubles(3333));